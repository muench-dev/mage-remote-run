export function getLogoForType(type) {
    if (type && type.startsWith('ac-')) {
        return logos.adobe;
    } else if (type === 'mage-os') {
        return logos.mageos;
    } else if (type === 'magento-os') {
        return logos.magento;
    }
    return '';
}
